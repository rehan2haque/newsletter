<p><span class="questionwhite">You’ve recently joined us, how are your finding your role as a business improvement specialist and your journey with kantar so far?</span>
So far it has been a breath of fresh air, from day one to date has been a real learning curve, there are so many different aspects of operations which I need to take the time to understand, but the experience has been a really exciting and rewarding one. Previously I worked in business improvement for larger organisations such as Chevron Texaco in the oil and gas industry in Aberdeen and Admiral Insurance in financial services in Cardiff. My role now incorporates a lot of the tools and methods that I used at previous organisations, but the environment in which I am using them feels a lot more relaxed and open. I find that the culture is much less focused on hierarchy and everyone in the organisation is extremely approachable which makes life a lot easier!</p>
<p><span class="questionwhite">So you’ve mentioned that you’ve worked in Aberdeen and Cardiff, I take it you have since moved to London?</span>
I have indeed. I’m a bit of a nomad. As a kid I lived in Colombia in South America and Azerbaijan in the Middle East and subsequently now I’m living in London.  So far I’m really enjoying living in London as it incorporates everything I love about the diverse places I’ve lived before, especially the Colombian Empanadas in Brixton Market!</p>

<p><span class="questionwhite">How important do you feel the Business and Process Improvement team is to delivering on our new operations identity and vision to drive client growth?</span>
Putting the client at the heart of operations strategy makes sense. Everything we do in operations is crucial to ensuring that we provide the client with the best quality product. The formation of the Business and Process Improvement team will help deliver more value to the client by consistently focusing on improving the way we do things – from coding a new barcode in Production to how we address panel members wants and needs in Panel Communications.</p>


<p><span class="questionwhite">If you could VPN into work from any spot in the world, where would it be?</span>
