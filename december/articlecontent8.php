<h3>Lifestyle Focus with Ian Mitchell</h3>	
<p>
The Worldpanel Lifestyle service is great. The depth of information available on shoppers and what they buy in the fashion, entertainment and telecoms markets is not available from other agencies, and our clients know this. We’ve been very successful as a business unit recently – the combined annual value of those services has grown by £800k in the last 3 years, from £4.2m in 2012 to £5m in 2015.
</p>
<br>
<p>
But we have an urgent need to improve our estimates of digital entertainment markets (which account for about a third of all home entertainment). They are quickly evolving and complex markets, and we find it hard to update what we collect from the panel fast enough to keep up with client demands and keep the survey simple enough for the panel to understand. 
</p>
<br>
<p>
Similarly, our telecoms survey is becoming unmanageable in size and arduous to complete each month. The result is confused panellists, inaccurate data and frustrated clients.  We must make the most of newer technology to make data collection from panellists easier, more accurate and more interesting.
</p>
<br>
<p>
In addition to this, it has been a long time since we developed any significantly new products to sell to clients – particularly in the fashion service, which uses a data set that looks a lot like what was used in 1995 when the service was created. We must change what we collect from the panel by focussing less on details about the products bought and more on details about the shoppers’ experience. 
</p>
<br>
<p>
The truth is, if we were setting up the Worldpanel Lifestyle service from scratch today, we wouldn’t make it look like it does now. That is what we have to have in mind with the Lifestyle Upgrade Project. The aim is to end up with more engaged panellists; more accurate data; newer, more relevant insights to sell; and the flexibility to adapt to future requirements.
</p>
